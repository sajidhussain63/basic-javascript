//Creating Variables in JS 

// Three keyword to declare variable in JS
// 1) var (Old one , Major issue for that we don't us it because it allows out of the block accessing)

var name='Sajid';
console.log(name);

// 2) let (Used in ES6 JS because this it does not allows accessing out of the block)
let age = 18;
console.log(age)

//const (used for giving a constant or not changable value to a variable)
const nationality='Pakistani';
console.log(nationality);

